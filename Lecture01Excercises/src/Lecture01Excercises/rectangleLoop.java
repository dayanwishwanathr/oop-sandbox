package Lecture01Excercises;

public class rectangleLoop {

    public static void main(String[] args) {
    	int i = 0;
    	
    	
    	
    	while(i<5) {
    		
    		int k=0;
    		
    		while(k<5) {
    			System.out.print("* ");
    			k++;
    		}
    		System.out.println();
    		i++;
    		
    	}
    	
    	
    }

}